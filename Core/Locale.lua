-- Mutate L in place (other files captured this exact table reference).
function QC.ApplyLocale(key)
	if not key or key == "auto" then
		key = (GetLocale and GetLocale()) or "enUS"
	end
	wipe(L)
	local t = translations[key]
	if t then
		for k, v in pairs(t) do rawset(L, k, v) end
	end
	QC.activeLocale = key
	if QC.Font and QC.Font.OnLocaleChanged then
		QC.Font.OnLocaleChanged()
	end
end

-- Apply game locale at load; OnInitialize re-applies any saved override.
QC.ApplyLocale("auto")
